// Helper functions to create DOM elements

// Create a general element with specified text
const createElement = (elementType, text) => {
  const element = document.createElement(elementType);
  element.textContent = text;
  return element;
};

// Create an image element with src and alt
const createImage = (url, alt) => {
  const img = document.createElement("img");
  img.src = url;
  img.alt = alt;
  return img;
};



// Create a header element with specified text and data-cy attribute
const createHeader = (headerType, text, dataCy) => {
  const header = document.createElement(headerType);
  header.textContent = text;
  header.setAttribute("data-cy", dataCy);
  return header;
};

// Create a button that toggles the visibility of a given element
const createToggleButton = (text, element) => {
  const button = document.createElement("button");
  button.textContent = text;
  button.addEventListener("click", () => {
    element.classList.toggle("notVisible");
  });
  return button;
};

// ===== Form Helper Functions =====

// Create a label element
const createLabel = (text, htmlFor) => {
  const label = document.createElement("label");
  label.textContent = text;
  label.setAttribute("for", htmlFor);
  return label;
};

// Create an input element
const createInput = (name) => {
  const input = document.createElement("input");
  input.name = name;
  input.id = name;
  input.type = "text";
  input.required = true; // <--
  return input;
};

// Create a submit button for a form
const createSubmitButton = (value) => {
  const submit = document.createElement("input");
  submit.setAttribute("data-cy", "submit-button"); // مهم لاختبار Cypress
  submit.type = "submit";
  submit.value = value;
  return submit;
};

export {
  createElement,
  createImage,
  createHeader,
  createToggleButton,
  createLabel,
  createInput,
  createSubmitButton,
};